// ── Mila request ───────────────────────────────────────────────────────────────

/**
 * MilaAssetRequest — the input to the Mila retrieval layer.
 *
 * Submitted by Mila when a customer message triggers a product lookup.
 * Typically extracted from the customer's NLP intent by the Mila agent.
 */
export interface MilaAssetRequest {
  /** Tenant isolation — required. */
  tenantId:          string;

  /**
   * The customer's intent in natural language.
   * Future: embedded for semantic search.
   * Current: used for keyword matching against asset tags/name.
   */
  customerIntent?:   string;

  /** Resolved product category from NLP. */
  category?:         ProductCategory;

  /** Extracted age range from customer message ("4 años", "talla 10"). */
  age?:              string;

  /** Extracted size ("talla 6", "XL", "26"). */
  size?:             string;

  /** Optional color filter ("azul", "rojo"). */
  color?:            string;

  /** Max number of assets to return. Default: 5 (WhatsApp carousel limit). */
  limit?:            number;

  /**
   * Preferred delivery channel for these assets.
   * Defaults to "whatsapp" — most Mila interactions are WhatsApp.
   */
  preferredChannel?: AssetChannel;

  /**
   * Whether to include pricing metadata in the result.
   * When true, only assets with price metadata are returned.
   */
  requirePricing?:   boolean;

  /**
   * Session context — the CRM conversation ID this request originates from.
   * Used for traceability and future personalization.
   */
  conversationId?:   string;
}

// ── Mila result ────────────────────────────────────────────────────────────────

/**
 * MilaAssetResult — a single asset returned to Mila for use in a response.
 */
export interface MilaAssetResult {
  assetId:      string;
  name:         string;
  thumbnailUrl?: string;
  /** Product price (formatted string, e.g. "$45.000"). */
  price?:       string;
  /** Available sizes (from metadata). */
  sizes?:       string[];
  /** Primary color or finish. */
  color?:       string;
  /** SKU for ordering / stock lookup. */
  sku?:         string;
  /** Relevance score for this result (0–1). */
  score:        number;
  /** Channel this asset is cleared for. */
  channel:      AssetChannel;
}

/**
 * MilaRetrievalResult — the full response from the Mila retrieval layer.
 */
export interface MilaRetrievalResult {
  tenantId:        string;
  assets:          MilaAssetResult[];
  total:           number;
  /** Whether results were filtered to WhatsApp-compatible assets. */
  whatsappFiltered: boolean;
  /** Whether the results were personalized to customer intent. */
  intentMatched:   boolean;
  /**
   * Suggested follow-up prompt for Mila to send to the customer.
   * E.g. "Tenemos estos productos en ropa niño..."
   * Future: generated by Mila agent from these results.
   */
  suggestedCaption?: string;
}

// ── WhatsApp catalog bundle ────────────────────────────────────────────────────

/**
 * WhatsAppCatalogBundle — a compact bundle of assets for a WhatsApp catalog message.
 *
 * WhatsApp catalogs have strict limits:
 *   - Max 10 products per message
 *   - Each image < 5 MB
 *   - Recommended: portrait format, clear product on clean background
 */
export interface WhatsAppCatalogBundle {
  tenantId:    string;
  title:       string;
  description?: string;
  items:       WhatsAppCatalogItem[];
  /** Total items in bundle. Max: 10. */
  count:       number;
  /** ISO timestamp when this bundle was assembled. */
  builtAt:     string;
}

export interface WhatsAppCatalogItem {
  assetId:     string;
  name:        string;
  price?:      string;
  sku?:        string;
  imageUrl:    string;
  thumbnailUrl?: string;
  /** WhatsApp catalog product ID (future: Catalogo de WhatsApp Business API). */
  waProductId?: string;
}

// ── Query translation ──────────────────────────────────────────────────────────

/**
 * resolveMilaQuery — translates a MilaAssetRequest into an AssetQuery.
 *
 * The Mila agent does not call the Biblioteca directly.
 * It calls this function to build the query, then executes via the query layer.
 */
export function resolveMilaQuery(req: MilaAssetRequest): AssetQuery {
  const channel   = req.preferredChannel ?? "whatsapp";
  const tokens:   string[] = [];

  if (req.customerIntent) {
    // Extract meaningful tokens from natural language intent
    req.customerIntent
      .toLowerCase()
      .split(/\s+/)
      .filter(t => t.length >= 3)
      .forEach(t => tokens.push(t.replace(/[^a-z0-9áéíóúñü]/g, "")));
  }
  if (req.age)   tokens.push(req.age.toLowerCase());
  if (req.size)  tokens.push(req.size.toLowerCase());
  if (req.color) tokens.push(req.color.toLowerCase());

  return {
    tenantId:   req.tenantId,
    intent:     "mila",
    query:      tokens.join(" ") || undefined,
    categories: req.category ? [req.category] : undefined,
    channels:   [channel],
    statuses:   ["approved", "published"],
    limit:      req.limit ?? 5,
    sortBy:     "usage",
    tags:       tokens.length > 0 ? tokens : undefined,
  };
}

/**
 * rankForCustomerIntent — ranks a list of assets for a specific Mila request.
 *
 * Applies WhatsApp-specific scoring and customer intent matching.
 * Returns the top N assets.
 */
export function rankForCustomerIntent(
  assets:  ScoringInput[],
  req:     MilaAssetRequest,
): ScoringInput[] {
  const limit     = req.limit ?? 5;
  const tokens    = buildIntentTokens(req);

  return assets
    .map(a => ({
      asset: a,
      score: scoreForMilaIntent(a, tokens, req.preferredChannel ?? "whatsapp"),
    }))
    .filter(({ score }) => score > 0.3)
    .sort((a, b) => b.score - a.score)
    .slice(0, limit)
    .map(({ asset }) => asset);
}

/**
 * getBestWhatsAppAssets — returns the top assets for WhatsApp display.
 *
 * Hard limit: 10 assets (WhatsApp catalog maximum).
 * Applies WhatsApp-specific scoring: file size, format, product clarity.
 */
export function getBestWhatsAppAssets(
  assets:  ScoringInput[],
  limit?:  number,
): ScoringInput[] {
  const max = Math.min(limit ?? 10, 10); // WhatsApp hard limit

  return assets
    .filter(a => a.status === "approved" || a.status === "published")
    .map(a => ({ asset: a, score: scoreForWhatsApp(a) }))
    .filter(({ score }) => score >= 0.5)
    .sort((a, b) => b.score - a.score)
    .slice(0, max)
    .map(({ asset }) => asset);
}

/**
 * buildWhatsAppCatalog — assembles a WhatsAppCatalogBundle from scored assets.
 *
 * Used when Mila needs to send a product catalog via WhatsApp Business API.
 */
export function buildWhatsAppCatalog(
  tenantId: string,
  title:    string,
  assets:   Array<{
    assetId:     string;
    name:        string;
    imageUrl:    string;
    thumbnailUrl?: string;
    price?:      string;
    sku?:        string;
  }>,
  description?: string,
): WhatsAppCatalogBundle {
  const items = assets.slice(0, 10).map(a => ({
    assetId:     a.assetId,
    name:        a.name,
    price:       a.price,
    sku:         a.sku,
    imageUrl:    a.imageUrl,
    thumbnailUrl: a.thumbnailUrl,
  }));

  return {
    tenantId,
    title,
    description,
    items,
    count:   items.length,
    builtAt: new Date().toISOString(),
  };
}

// ── Helpers ───────────────────────────────────────────────────────────────────

function buildIntentTokens(req: MilaAssetRequest): string[] {
  const tokens: string[] = [];
  if (req.customerIntent) {
    req.customerIntent.toLowerCase().split(/\s+/).filter(t => t.length >= 3).forEach(t => tokens.push(t));
  }
  if (req.age)   tokens.push(req.age.toLowerCase());
  if (req.size)  tokens.push(req.size.toLowerCase());
  if (req.color) tokens.push(req.color.toLowerCase());
  return tokens;
}

function scoreForMilaIntent(
  asset:   ScoringInput,
  tokens:  string[],
  channel: AssetChannel,
): number {
  let score = scoreForWhatsApp(asset);

  // Bonus for channel match
  if (asset.channels.includes(channel)) score += 0.2;

  // Bonus for token match
  if (tokens.length > 0) {
    const searchable = [asset.name ?? "", asset.sku ?? "", ...(asset.tags ?? [])].join(" ").toLowerCase();
    const matched    = tokens.filter(t => searchable.includes(t)).length;
    score += (matched / tokens.length) * 0.3;
  }

  return Math.min(1, score);
}
