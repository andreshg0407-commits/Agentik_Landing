// ── Trend analysis ────────────────────────────────────────────────────────────

/**
 * Period-over-period comparison of key commercial metrics.
 *
 * Copilot: "statistics.getTrendAnalysis()"
 *
 * Scenarios:
 *   "¿Qué está empeorando?"
 *     → all ComparisonMetrics where direction="down", sort pct asc
 *
 *   "¿Las ventas subieron esta semana?"
 *     → revenue.direction + revenue.pct
 */
export interface TrendAnalysis {
  period:   StatisticsPeriod;
  fromDate: string;   // ISO8601 start of current period
  toDate:   string;   // ISO8601 end of current period
  revenue:  ComparisonMetric;
  orders:   ComparisonMetric;
  aov:      ComparisonMetric;
  units:    ComparisonMetric;
  refunds:  ComparisonMetric;
  returns:  ComparisonMetric;
}

// ── Executive insights ────────────────────────────────────────────────────────

/**
 * A structured executive insight produced by deterministic threshold rules.
 *
 * NON-AI GUARANTEE: Never generated by a language model.
 * Computed from fixed rule evaluation against real metric data.
 * Copilot uses these as inputs to its reasoning, not as outputs.
 *
 * Used by:
 *   - Attention Layer (right rail signal strip)
 *   - Copilot reasoning engine input
 *   - Future: executive council synthesis
 *
 * Copilot read-aloud:
 *   "[{severity.toUpperCase()}] {title}: {description}
 *    Evidencia: {evidence.join(' | ')}
 *    Acción: {suggestedAction}"
 */
export interface ExecutiveInsight {
  /** Unique ID within this analysis run. */
  id:              string;
  category:        InsightCategory;
  title:           string;
  description:     string;
  severity:        InsightSeverity;
  impact:          InsightImpact;
  /** 1 = most urgent. Controls display and Copilot priority queue order. */
  priority:        InsightPriority;
  /** Human-readable data points supporting this insight. */
  evidence:        string[];
  /**
   * Copilot-ready action prompt. Null if purely informational.
   * Copilot MUST show to user and await approval before executing.
   */
  suggestedAction: string | null;
}

// ── Metric health summary ─────────────────────────────────────────────────────

/**
 * Copilot-ready health snapshot covering all key commercial KPIs.
 *
 * Designed for direct Copilot consumption without additional transformation.
 * All HealthedMetric values are pre-classified using deterministic rules.
 *
 * Named properties allow direct Copilot field access:
 *   healthSummary.revenue.metricHealth        → "warning"
 *   healthSummary.failedPayments.healthReason → "2 pedidos con pagos fallidos..."
 *
 * Ranked lists enable priority-sorted responses:
 *   needsAttention[0] → highest-priority KPI requiring action
 *   improving[0]      → best-performing KPI this period
 *
 * Copilot scenarios:
 *   "¿Qué indicadores requieren atención?"
 *     → needsAttention (pre-sorted: critical first, then warning)
 *
 *   "¿Qué mejoró esta semana?"
 *     → improving
 *
 *   "Dame un semáforo de la tienda."
 *     → all 7 named KPIs with metricHealth and healthReason
 *
 *   "¿Dónde debo actuar primero?"
 *     → needsAttention[0].healthReason + needsAttention[0].label
 *
 * NON-AI GUARANTEE: All classifications are rule-based, never AI-generated.
 */
export interface MetricHealthSummary {
  /** Commercial revenue health. */
  revenue:            HealthedMetric;
  /** Order volume health. */
  orders:             HealthedMetric;
  /** Average order value health. */
  averageOrderValue:  HealthedMetric;
  /** Refund amount health (negative metric — rising is bad). */
  refunds:            HealthedMetric;
  /** Return order count health (negative metric — rising is bad). */
  returns:            HealthedMetric;
  /**
   * Failed payment count health.
   * Any > 0 → warning. > 3 → critical.
   * Uses current value vs 0 when no historical data is available.
   */
  failedPayments:     HealthedMetric;
  /**
   * Delayed shipment count health.
   * Any > 0 → warning. > 5 → critical.
   */
  delayedShipments:   HealthedMetric;
  /**
   * KPIs classified as "warning" or "critical", sorted:
   * critical first, then warning, then by deltaPercent (absolute) descending.
   * Pre-sorted — Copilot can use index 0 as the highest-priority signal.
   */
  needsAttention:     HealthedMetric[];
  /**
   * KPIs classified as "good" with direction="up".
   * Sorted by deltaPercent descending — best improvement first.
   */
  improving:          HealthedMetric[];
}

// ── Full overview ─────────────────────────────────────────────────────────────

/**
 * Full executive statistics overview for a Shopify store.
 * Primary output of getOverview() — the top-level analytics contract.
 *
 * Three consumers:
 *   1. Executive workspace (UX statistics screen)
 *   2. Copilot: "Dame el resumen ejecutivo de la tienda."
 *   3. Automation: anomaly detection → alert → Copilot action proposal
 *
 * Copilot: "statistics.getOverview()"
 */
export interface StatisticsOverview {
  sales:       SalesMetrics;
  catalog:     CatalogMetrics;
  promotions:  PromotionMetrics;
  operations:  OperationsMetrics;
  funnel:      FunnelMetrics;
  trends:      TrendAnalysis;
  /** Sorted by priority ascending (1 = most urgent first). */
  insights:    ExecutiveInsight[];
  /**
   * Deterministic health classification for all key KPIs.
   * Primary input for Copilot health queries and right-rail attention signals.
   * NON-AI: all values computed from threshold rules against real metric data.
   */
  healthSummary: MetricHealthSummary;
  generatedAt: string;   // ISO8601
  source:      "shopify";
  period:      StatisticsPeriod;
}
