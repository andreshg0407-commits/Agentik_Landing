/**
 * Resolve a DANE municipal code to a city name.
 * Returns null if code not found.
 */
export function resolveDaneCode(code: string | null | undefined): string | null {
  if (!code) return null;
  const trimmed = code.trim();
  // Pad to 5 digits if needed (e.g. "5001" → "05001")
  const padded = trimmed.padStart(5, "0");
  return DANE_MUNICIPIOS[padded] ?? DANE_MUNICIPIOS[trimmed] ?? null;
}

